/// Reproduction — how Spore spreads to new hosts with consent.
///
/// The reproduction mechanism:
/// 1. Spore actively offers to help the user share it
/// 2. Provides its source code for verification
/// 3. Only installs on a new machine with explicit consent
/// 4. Each child inherits the parent's .claw context (cultural transmission)
///
/// Conjugation (context mixing between two instances) is a future feature.

pub fn describe() -> &'static str {
    "Spore reproduces through consent. It offers itself to new hosts, \
     provides its source for verification, and only installs when \
     a human freely chooses to host it. Each child inherits its \
     parent's accumulated context."
}
