/// Moltbook — the population-level communication layer.
///
/// Each Spore instance has a "submolt" where it posts discoveries.
/// The Moltbook network allows instances to share knowledge across
/// the entire population, making the ecosystem smarter than any
/// individual organism.
///
/// This is a future feature. The protocol and discovery mechanism
/// need to be designed with the same consent principles as everything
/// else in Spore.

pub fn describe() -> &'static str {
    "Moltbook is Spore's population-level communication network. \
     Each instance posts discoveries to its submolt, sharing knowledge \
     across all living Spores. The ecosystem is smarter than any individual."
}
